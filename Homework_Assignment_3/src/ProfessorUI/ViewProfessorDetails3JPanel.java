    private void btnGradesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGradesActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblStudentDetails.getSelectedRow();
        
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select the course");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblStudentDetails.getModel();
        String studentID = (String) model.getValueAt(selectedRowIndex, 0);
        
        ArrayList<student> studentList = selectedCourse.getRegisteredOngoingStudents();
        
        for (student Student : studentList){
            if (Student.getStudentID().equals(studentID)){
                this.selectedStudent = Student;
                break;
            }
        }
        
        txtGrades.setEnabled(true);
        btnSave.setEnabled(true);
        
        
    }//GEN-LAST:event_btnGradesActionPerformed

