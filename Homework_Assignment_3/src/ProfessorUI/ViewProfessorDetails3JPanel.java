    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        Double grades = Double.parseDouble(txtGrades.getText());
        if (grades < 0 || grades > 4){
            JOptionPane.showMessageDialog(this, "Please enter valid grades");
            return;
        }
        this.selectedStudent.addcompletedCourses(selectedCourse);
        selectedCourse.addPastStudentToCourse(selectedStudent);
        this.selectedStudent.addCompletedCoursesGrades(grades);
        JOptionPane.showMessageDialog(this, "Student graded");
        txtGrades.setEnabled(false);
        btnSave.setEnabled(false);
        
        populateStudentDetailsTable();
        
    }//GEN-LAST:event_btnSaveActionPerformed


